import java.lang.reflect.Array;

public class Datatypes {
    public static void main(String[] args) {

        int a = 20;
        a = 40;  // Reassigning the value of 'a'
        System.out.println(a);  // Output: 40

        float b = 41.54f;
        System.out.println(b);  // Output: 41.54

        String c = "Sanket";
        System.out.println(c);  // Output: Sanket

        boolean d = true;
        System.out.println(d);  // Output: true

        Array B1;  // This declares a variable of type java.lang.reflect.Array, but it's not initialized.
    }
}
